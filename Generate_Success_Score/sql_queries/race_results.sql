SELECT
  RES.RACEID,
  RES.DRIVERID,
  RES.CONSTRUCTORID,
  RES.GRID,
  RES.POSITIONORDER,
  RES.POINTS,
  RES.LAPS,
  RES.FASTESTLAPSPEED,
  RES.FASTESTLAPTIME,
  RES.STATUSID,
  STAT.STATUS
FROM PLAYGROUND.DATATHON.RESULTS RES
RIGHT JOIN PLAYGROUND.DATATHON.STATUS STAT
  ON STAT.STATUSID = RES.STATUSID
WHERE RES.RACEID BETWEEN 175 AND 1144
ORDER BY RES.RACEID, RES.POSITIONORDER;